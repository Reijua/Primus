	<p>
		Ein Cookie ist eine kleine Textdatei, die auf Ihrem Computer, Ihrem Mobiltelefon oder einem anderen Gerät abgelegt wird, wenn Sie eine Webseite öffnen. Es hilft den Webseite-Anbietern dabei, Ihr Gerät zu erkennen, wenn Sie sich wieder auf die Webseite begeben. Primus Romulus verwendet den Begriff „Cookie“ in dieser Datenschutzerklärung als Oberbegriff für Cookies und alle ähnlichen Technologien, die auf diese Weise Informationen erfassen.
	</p>

